// defines where a function, class or variable is visible in a project
// kotlin gives 4 modifiers : public, private, internal, protected
// a public class/func./var is accessible everywhere
// a private func is accessible only in same class where it's defined, a private var is accessible in same file
// protected = same as private, but also accessible in subclasses
// internal = access in same module

// If you override a protected or an internal member and do not specify the visibility explicitly...
// ...the overriding member will also have the same visibility as the original

